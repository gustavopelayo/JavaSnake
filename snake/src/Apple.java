public class Apple {

    private int xCoor;
    private int yCoor;

    public Apple(int xCoor, int yCoor){

        this.xCoor = xCoor;
        this.yCoor = yCoor;
    }

    public int getxCoor() {
        return xCoor;
    }

    public int getyCoor() {
        return yCoor;
    }
}
